# append()
x = [10, 20]
x.append(30)
print(x)

# insert()
x = [10, 20]
x.insert(1, 99)
print(x)

# remove()
x = ["a", "b", "c"]
x.remove("b")
print(x)

# pop()
x = ["a", "b", "c"]
x.pop(0)
print(x)

# pop()
x = [10,20,30]
y = x.pop()
print(y)
print(x)

# duplicate value check
x = [1, 2, 2, 3]
x.remove(2)
print(x)